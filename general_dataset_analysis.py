import pandas as pd
import numpy as np
import re
import pandas as pd
from matplotlib import pyplot as plt
import seaborn as sns


df = pd.read_csv("IMDb_Clean_CSV.csv")

df.head()

# Check for missing values in the dataframe while ignoring "None"
print((df.isnull() & (df == 'None')).sum())

plt.figure(figsize=(12,6))
sns.histplot(x=df['Year'])
plt.ylabel("Number of Movies")
plt.tight_layout()

# Plotting examples
# Example 1: Histogram of Ratings
plt.figure(figsize=(9, 5))
sns.histplot(df['Rating'], kde=True, bins=20)
plt.title('Distribution of Ratings')
plt.xlabel('Rating')
plt.ylabel('Frequency')
plt.show()

# Example 3: Countplot of Genres
plt.figure(figsize=(10, 5))
sns.countplot(y='Genre', data=df, order=df['Genre'].value_counts().index)
plt.title('Count of Movies by Genre')
plt.xlabel('Count')
plt.ylabel('Genre')
plt.show()

# Get the top 25 directors based on the count of their movies
top_directors = df['Director'].value_counts().head(25)

# Plotting the graph
plt.figure(figsize=(9, 5))
sns.barplot(x=top_directors.values, y=top_directors.index, palette="mako")
plt.title('Top 25 Directors')
plt.xlabel('Number of Movies')
plt.ylabel('Director')
plt.show()

# Example 2: Boxplot of Runtime
plt.figure(figsize=(18, 15))
sns.boxplot(x='Censor Rating', y='Runtime (Mins)', data=df)
plt.title('Boxplot of Runtime by Censor Rating')
plt.xlabel('Censor Rating')
plt.ylabel('Runtime (Mins)')
plt.show()